package model;

public class ShakeSpearModel {

    private int line_id;
    private String play_name;
    private String speech_number;
    private String line_number;
    private String speaker;
    private String text_entry;

    public int getLine_id() {
        return line_id;
    }

    public void setLine_id(int line_id) {
        this.line_id = line_id;
    }

    public String getPlay_name() {
        return play_name;
    }

    public void setPlay_name(String play_name) {
        this.play_name = play_name;
    }

    public String getSpeech_number() {
        return speech_number;
    }

    public void setSpeech_number(String speech_number) {
        this.speech_number = speech_number;
    }

    public String getLine_number() {
        return line_number;
    }

    public void setLine_number(String line_number) {
        this.line_number = line_number;
    }

    public String getSpeaker() {
        return speaker;
    }

    public void setSpeaker(String speaker) {
        this.speaker = speaker;
    }

    public String getText_entry() {
        return text_entry;
    }

    public void setText_entry(String text_entry) {
        this.text_entry = text_entry;
    }


}
